  static List<Map<String, dynamic>> getAllTemplates() {
    return khonologyTemplates;
  }

  static List<Map<String, dynamic>> getTemplatesByCategory(String category) {
    return khonologyTemplates
        .where((template) => template['category'] == category)
        .toList();
  }

  static Map<String, dynamic>? getTemplateById(String id) {
    try {
      return khonologyTemplates.firstWhere((template) => template['id'] == id);
    } catch (e) {
      return null;
    }
  }

  static List<String> getCategories() {
    return khonologyTemplates
        .map((template) => template['category'] as String)
        .toSet()
        .toList();
  }

  static List<Map<String, dynamic>> searchTemplates(String query) {
    final lowercaseQuery = query.toLowerCase();
    return khonologyTemplates.where((template) {
      return template['name']
              .toString()
              .toLowerCase()
              .contains(lowercaseQuery) ||
          template['description']
              .toString()
              .toLowerCase()
              .contains(lowercaseQuery) ||
          template['category']
              .toString()
              .toLowerCase()
              .contains(lowercaseQuery);
    }).toList();
  }
